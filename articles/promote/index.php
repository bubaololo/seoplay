                <h2 class="title">Полезные статьи про SEO</h2>
                <p class="articles__text">
                    Создавая сайт для бизнеса, это вполне очевидно, что предприниматели ждут получения прибыли. Но просто создать сайт не работает. В 2021 году зарабатывать с помощью сайта невозможно без использования SEO. В этой статье, мы расскажем что такое SEO, а также
                    объясним преимущества, особенности и тонкости данного метода по сравнению с другими способами продвижения сайта.
                </p>
                <h2 class="title">Что такое SEO и как оно работает?</h2>
                <p class="articles__text">
                    Прежде, чем мы перейдем к тому зачем бизнесу который решил зарабатывать в интернете нужно СЕО, давайте выясним как работают поисковые системы и роботы и разберем что такое SEO-позиционирование.
                </p>
                <h3 class="subtitle">Что такое поисковый робот?</h3>
                <p class="articles__text">Это программа (бот, краулер, веб-паук) для сканирования и индексации сайтов, который позволяет поисковой системе получить сведения о страницах сайта и внести их в базу. При этом поисковые роботы не анализируют данные, а только передает
                    их на сервера для последующей выдачи пользователям при запросе. Для ускорения индексации создаются файлы robots.txt и XML Sitemap.</p>
                <p class="articles__text">После все данные обрабатываются и проверяются на соблюдение и соответствие правилам поисковой системы например - Google. Таким образом формируется выдача в 3 этапа:</p>
                <dl>
                    <dt>Сканирование</dt>
                    <dd>(происходит регулярно с учетом частоты обновлений ресурса).</dd>
                    <dt>Индексация</dt>
                    <dd>внесение собранной информации в базу данных с присвоением определенного индекса.</dd>
                    <dt>Выдача результатов</dt>
                    <dd>поиск информации по индексу и ранжирование сайтов/страниц с учетом релевантности запросу.</dd>
                </dl>
                <h3 class="subtitle">Что такое SEO ?</h3>
                <p class="articles__text">SEO — это сокращение от английского Search Engine Optimization или оптимизация под поисковые системы. То есть это изменение и адаптация сайта не только под ожидания и потребности пользователей, но и под поисковых роботов. Все действия
                    SEO направлены на повышение позиций сайта в органической выдаче (бесплатно) в поисковых системах таких как Google, Яндекс, Yahoo, Bing, и др. по определенным запросам пользователей.<br><br>Итак, SEO-позиционирование — это создание
                    образа компании, позволяющее выделить ее на фоне конкурентов с использованием определенных техник для повышения видимости вашего сайта в выдаче Google.</p>
                <h3 class="subtitle">Как работает SEO (оптимизация сайта)?</h3>
                <p class="articles__text">Поисковые системы постоянно обновляются для улучшения качества выдачи, чтобы пользователь тратил минимально времени для поиска необходимой информации. Поэтому SEO подразумевает работу над сайтом комплексно: работа с технической стороной,
                    контентом, юзабилити, удобством, адаптивной версией сайта под различные устройства, привлечением естественных ссылок и так далее.</p>
                <ul>В основном работы делятся на 2 подвида:
                    <li>Внутренняя оптимизация;</li>
                    <li>Внешняя оптимизация.</li>
                </ul>
                <h4>Внутренняя оптимизация</h4>
                <p class="articles__text">Это самая трудоемкая и цикличная работа, которую необходимо проводить постоянно. Все действия направлены на улучшение и оптимизации самого ресурса (сайта), которые влияют на его позицию в выдаче. Именно от внутренней оптимизации зависит
                    успех сайта — он должен быть качественным, удобным для пользователя, быстрым, и в полной мере предоставлять именно ту информацию, за которой пришел пользователь.</p>
                <ul>Сюда входят следующие работы:
                    <li>Составление семантического ядра (подбор ключевых слов, по которым планируется продвижение ресурса);</li>
                    <li>Улучшение структуры сайта (карта сайта, Robots.txt);</li>
                    <li>Выявление и устранение технических ошибок (битые ссылки, дубли страниц, увеличение скорости загрузки сайта);</li>
                    <li>Повышение юзабилити (удобство для пользователей);</li>
                    <li>Повышение релевантности страниц и контента;</li>
                    <li>Реализация внутренней перелинковки, чтобы не было «тупиковых страниц»;</li>
                    <li>Оптимизация всего контента, включая изображения.</li>
                </ul>
                <p class="articles__text">Оптимизация и работа над UX/UI — это дополнительная выгода для бизнеса. Чем более привлекательно выглядит сайт, чем более он удобен в использовании и чем более он релевантен запросу, тем дольше времени проводит пользователь на сайте, повышается
                    доверие пользователя к контенту как к профессионалам своего дела, а это в свою очередь генерирует больше лидов и повышает ключевые метрики сайта.</p>
                <h4>Внешняя оптимизация</h4>
                <p class="articles__text">Это наращивание ссылочной массы — получение ссылок на свой сайт с других ресурсов с хорошей репутацией (хорошие сайты-доноры). Также стоит использовать методы получения ссылок, не противоречащие правилам поисковых систем:</p>
                <ul>
                    <li>Публикация интересных и релевантных материалов, статей, исследований, кейсов, которыми будут делиться пользователи;</li>
                    <li>Обмен ссылками с популярными, трастовыми и качественными ресурсами;</li>
                    <li>Регистрация в поисковых системах и каталогах;</li>
                    <li>Размещение пресс-релизов;</li>
                    <li>Активность в блогах и на форумах релевантных проекту;</li>
                    <li>Анализ конкурентов для выявления точек роста.</li>
                </ul>
                <h3 class="subtitle">Виды SEO</h3>
                <p class="articles__text">Существует несколько методов оптимизации сайтов: белое, серое, и черное SEO, в зависимости от того, соблюдаются ли правила поисковых систем. Это означает, что мастер SEO, который знает определенные алгоритмы поисковых систем, использует
                    те или иные техники, которые влияют на работу робота для продвижения сайта в ТОП выдачи.<br><br>На сегодняшний день многие сеошники используют все методики, но тут важно понимать все риски. И вот главные отличия методов.
                </p>
                <ol>
                    <li>
                        <h3 class="subtitle">Белое SEO</h3>
                        <p class="articles__text">Эти техники стоит применять изначально, так как они направлены на оптимизацию всего сайта и создание нормальных условий для пользователей:</p>
                        <ul>
                            <li>Повышение юзабилити сайта (UX/UI) и улучшение пользовательского опыта так такового;</li>
                            <li>Предоставление качественного и полезного контента для удержания пользователей и привлечения новой аудитории;</li>
                            <li>Продумывание дизайна, верстки, подбору цветовой гаммы, и т.д.;</li>
                            <li>Размещение гостевых постов с ссылкой на ресурс;</li>
                            <li>Нативная реклама, и прочее.</li>
                        </ul>
                        <p class="articles__text">Попросту создается действительно качественный продукт с заботой о клиенте, учетом болей и проблем пользователей.</p>
                    </li>
                    <li>
                        <h3 class="subtitle">Серое SEO</h3>
                        <p class="articles__text">Это наиболее распространенный вид SEO, которое использует большинство SEO-специалистов. Эти методики не нарушают правила и подразумевают работу с уже существующими и действующими поисковыми запросами реальных пользователей — ключевыми
                            фразами и словами («ключевиками»). По сути это:</p>
                        <ul>
                            <li>Использование и/или внедрение ключевых фраз и слов в контент сайта (написание SEO-текстов, например);</li>
                            <li>Внедрение ключевых фраз в HTML-теги;</li>
                            <li>Сбор поисковых запросов, создание иерархии запросов, а также правильное количественное употребление и грамотное распределение таких «ключевиков» по тексту.</li>

                        </ul>
                        <p class="articles__text">Данный метод показывает хорошие результаты, но стоит его применять с умом и осторожностью, чтобы не попасть под фильтр или другие штрафы со стороны поисковых систем.</p>
                    </li>

                    <li>
                        <h3 class="subtitle">Черное SEO</h3>
                        <p class="articles__text">Если с умом, то можно применять и эти методики, но это не рекомендуется так как можно попасть под жесткий фильтр поисковых систем. По итогу, применение данных SEO схем на практике может выйти дороже, чем если использовать более
                            долгий, но более безопасный путь белого и/или серого СЕО. Поэтому следующая информация просто для ознакомления. Вот несколько способов черной оптимизации сайта:</p>
                        <ol>
                            <li>Использование подставных страниц, которые прописываются в файле robots.txt для анализа поисковыми роботами и дальнейшей индексации, но по итогу пользователь на самом деле попадает и видит совсем другие страницы.</li>
                            <li>Создание дорвеев или сайтов, которые максимально заточены для выхода в ТОП-выдачи, и дальнейшая переадресация на основной ресурс, который нужно продвигать.</li>
                            <li>Использование сайтов-спутников. Создается множество лендингов, одностраничников и небольших сайтов, на которых размещаются ссылки на основной ресурс. Таким образом создается ссылочная масса для повышения рейтинга сайта и его
                                дальнейшего продвижения в ТОП-выдачи.</li>
                        </ol>
                    </li>
                </ol>
